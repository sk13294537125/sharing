                                              `order_id` varchar(32) DEFAULT NULL COMMENT '订单号',
                                              `reserve_id` varchar(32) DEFAULT NULL COMMENT '预约单id（首博返回）',
                                              `reserve_no` varchar(32) DEFAULT NULL COMMENT '预约单编号（首博返回）',
                                              `pay_type` varchar(2) DEFAULT NULL COMMENT '支付类型：0-免费，1-待支付，2-支付成功',
                                              `reserve_type` varchar(2) DEFAULT NULL COMMENT '预约类型：0-卡类型预约、1-证件类型预约',
